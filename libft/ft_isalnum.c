#include "libft.h"

int	ft_isalnum(int let)
{
	if ((let <= 'z' && let >= 'a') || (let <= 'Z' && let >= 'A'))
		return (1);
	if (let <= '9' && let >= '0')
		return (1);
	return (0);
}
